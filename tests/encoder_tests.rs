#[test]
fn test_encoder_model_clone() {
    let encoder_model = ENCODER_MODEL.clone_model();
    let first_cluster_value = encoder_model.centroids.get(&[0, 0]);
    assert_eq!(format!("{:.3}", first_cluster_value), "-0.543");
}

